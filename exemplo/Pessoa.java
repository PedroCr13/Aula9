public class Pessoa {
	
	private int rg;
	private String nome;
	private int salario;
	
	public void setRg(int rg){
		this.rg = rg;
	}
	
	public void setNome(String nome){
		this.nome = nome;
	}
	
	public void setSalario(int salario){
		this.salario = salario;
	}
	
	public int getRg(){
		return rg;
	}
	
	public String getNome(){
		return nome;
	}
	
	public int getSalario(){
		return salario;
	}
}